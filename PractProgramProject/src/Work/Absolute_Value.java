public class Absolute_Value {

	static void findAbsolute(int N) 
	{ 
	  
	    // If the number is less than 
	    // zero, then multiply by (-1) 
	    if (N < 0)  
	    { 
	        N = (-1) * N; 
	    } 
	  
	    // Print the absolute value 
	    System.out.printf("%d ", N); 
	} 
}
